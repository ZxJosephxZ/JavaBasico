package EjerciciosJava;

public class Ejercicio1 {
    public static void main(String[] args) {
        //EJERCICIO BASICO: Mostrar por consola operaciones basicas
        int numero1 = 10;
        int numero2 = 1;
        System.out.println(numero1 + "+" + numero2 + " = " + (numero1+numero2));
        System.out.println(numero1 + "-" + numero2 + " = " + (numero1-numero2));
        System.out.println(numero1 + "*" + numero2 + " = " + (numero1*numero2));
        System.out.println(numero1 + "/" + numero2 + " = " + (numero1/numero2));
        System.out.println(numero1 + "%" + numero2 + " = " + (numero1%numero2));
    }
}
